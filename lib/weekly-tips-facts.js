// Helper function to get current week's content
export function getCurrentWeekContent() {
  // Get week number (1-52) based on current date
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 1);
  const weekNumber = Math.ceil(((now - start) / 86400000 + start.getDay() + 1) / 7);
  
  // Ensure week number is between 1 and 52
  const week = weekNumber > 52 ? 52 : weekNumber;
  
  return {
    strategy: weeklyStrategies.find(s => s.week === week) || weeklyStrategies[0],
    fact: weeklyFacts.find(f => f.week === week) || weeklyFacts[0]
  };
}

